package day4;

public class Employee  {
	private int empId;
	private String name;
	private double salary;
	
	
	//No-Argument Constructor
	public Employee() {
		
		
	}

	//Parameterized Constructor
	public Employee(int empId, String name, double salary) {
		
		this.empId = empId;
		this.name = name;
		this.salary = salary;
	}
	
	
	
	
	
}
